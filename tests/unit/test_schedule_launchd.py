def test_inspect_missing_file_when_artifact_absent(emitter: LaunchdEmitter, tmp_path: Path) -> None:
    """``"missing-file"`` when the plist file does not exist on disk."""
    absent = tmp_path / "com.llm-wiki-kit.aabbcc.nonexistent.plist"
    # Do not create the file.
    result = emitter.inspect(absent)
    assert result == "missing-file"


def test_inspect_loaded_when_launchctl_print_succeeds(
    emitter: LaunchdEmitter,
    artifact_file: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """``"loaded"`` when ``launchctl print`` exits 0."""

    def mock_run(cmd: list[str], **kwargs: object) -> subprocess.CompletedProcess[str]:
        # Simulate a successful ``launchctl print gui/<uid>/<label>`` response.
        label = "com.llm-wiki-kit.abcdef123456.test-op"
        return subprocess.CompletedProcess(
            args=cmd,
            returncode=0,
            stdout=(
                "{\n"
                "\tactive count = 1\n"
                f"\tpath = /Users/user/Library/LaunchAgents/{label}.plist\n"
                "\tstate = waiting\n"
                "}\n"
            ),
            stderr="",
        )

    monkeypatch.setattr("llm_wiki_kit.schedule.launchd.subprocess.run", mock_run)
    result = emitter.inspect(artifact_file)
    assert result == "loaded"


def test_inspect_not_loaded_when_launchctl_print_fails(
    emitter: LaunchdEmitter,
    artifact_file: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """``"not-loaded"`` when ``launchctl print`` exits non-zero.

    This covers the post-bootout state where the plist file is still on
    disk but launchd no longer has the service registered.
    """

    def mock_run(cmd: list[str], **kwargs: object) -> subprocess.CompletedProcess[str]:
        return subprocess.CompletedProcess(
            args=cmd,
            returncode=113,
            stdout="",
            stderr="Could not find service "
            '"com.llm-wiki-kit.abcdef123456.test-op" in domain for port\n',
        )

    monkeypatch.setattr("llm_wiki_kit.schedule.launchd.subprocess.run", mock_run)
    result = emitter.inspect(artifact_file)
    assert result == "not-loaded"


def test_inspect_not_loaded_covers_system_restart_state(
    emitter: LaunchdEmitter,
    artifact_file: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """Non-zero from ``launchctl print`` maps to ``"not-loaded"`` regardless of message."""

    def mock_run(cmd: list[str], **kwargs: object) -> subprocess.CompletedProcess[str]:
        return subprocess.CompletedProcess(
            args=cmd,
            returncode=1,
            stdout="",
            stderr="",
        )

    monkeypatch.setattr("llm_wiki_kit.schedule.launchd.subprocess.run", mock_run)
    result = emitter.inspect(artifact_file)
    assert result == "not-loaded"


