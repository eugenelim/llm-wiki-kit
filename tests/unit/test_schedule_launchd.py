"""Tests for ``llm_wiki_kit.schedule.launchd``.

Covers the four mandated cases from plan step 4 in
``docs/specs/wiki-schedule/plan.md``:

1. ``render_artifact()`` golden-string assertions for each cadence kind
   (daily / weekly / monthly / quarterly). The weekly golden string for
   ``TUE 18:00`` also pins spec CT-2's ``Weekday=2 Hour=18 Minute=0``
   encoding.
2. For a quarterly cadence, the rendered plist's ``StartCalendarInterval``
   is a four-element array of dicts whose ``Month`` keys are
   ``(1, 4, 7, 10)``. Pins that the emitter hands the full
   ``to_launchd_calendar_interval(...)`` list to ``plistlib``, not just
   ``intervals[0]``.
3. The argv-block contents pin ``wiki run --exec <op>`` shape in
   ``ProgramArguments``.
4. ``inspect()`` returns each of the four ``InspectResult`` states given
   fixture ``launchctl print`` outputs (mocked via subprocess monkeypatch
   of the ``llm_wiki_kit.schedule.launchd`` module, per the style
   convention in ``schedule/_emitter.py``).

Integration test (``@pytest.mark.slow``, Darwin-only) lives in
``tests/integration/test_schedule_launchd_macos.py``.
"""

