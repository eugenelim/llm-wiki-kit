# ---------------------------------------------------------------------------
# 2. Quarterly StartCalendarInterval — four-element list with Month keys (1,4,7,10)
# ---------------------------------------------------------------------------


def test_quarterly_start_calendar_interval_four_elements(emitter: LaunchdEmitter) -> None:
    """Pins that the emitter hands the full ``to_launchd_calendar_interval``
    list to plistlib, not just ``intervals[0]``.

    Parsing the rendered bytes back through plistlib gives the authoritative
    view of what launchd will read.
    """
    cadence = ResolvedCadence(period="quarterly", hour=9, minute=0, day_of_month=1)
    result = emitter.render_artifact(
        operation="quarterly-op",
        vault_root=VAULT_ROOT,
        vault_id=VAULT_ID,
        cadence=cadence,
        exec_command=["/usr/local/bin/wiki", "run", "--exec", "quarterly-op"],
    )
    parsed = plistlib.loads(result)
    intervals = parsed["StartCalendarInterval"]
    assert isinstance(intervals, list), "StartCalendarInterval must be a list (array), not a dict"
    assert len(intervals) == 4, f"expected 4 intervals for quarterly, got {len(intervals)}"
    months = [entry["Month"] for entry in intervals]
    assert months == [1, 4, 7, 10], f"expected months (1,4,7,10), got {months}"


